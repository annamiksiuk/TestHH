import UIKit

extension UIFont {
    
    class func SFUITextMedium(size: CGFloat) -> UIFont {
        return UIFont(name: "SFUIText-Medium", size: size)!
    }
    
    class func SFUITextRegular(size: CGFloat) -> UIFont {
        return UIFont(name: "SFUIText-Regular", size: size)!
    }
    
}
